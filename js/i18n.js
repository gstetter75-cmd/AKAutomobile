/* ---------- i18n Engine ---------- */
function initI18n() {
  const savedLang = localStorage.getItem('ak-lang') || 'de';
  setLanguage(savedLang);

  const toggle = document.getElementById('langToggle');
  if (toggle) {
    toggle.addEventListener('click', () => {
      const current = document.documentElement.getAttribute('lang') || 'de';
      const next = current === 'en' ? 'de' : 'en';
      setLanguage(next);
    });
  }
}

function setLanguage(lang) {
  const dict = translations[lang];
  if (!dict) return;

  document.documentElement.setAttribute('lang', lang);
  localStorage.setItem('ak-lang', lang);

  // Update toggle button text
  const toggle = document.getElementById('langToggle');
  if (toggle) {
    toggle.textContent = lang === 'de' ? 'EN' : 'DE';
    toggle.setAttribute('aria-label', lang === 'de' ? 'Switch to English' : 'Auf Deutsch wechseln');
  }

  // Update all [data-i18n] elements
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (dict[key] !== undefined) {
      el.innerHTML = dict[key];
    }
  });

  // Update all [data-i18n-placeholder] elements
  document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
    const key = el.getAttribute('data-i18n-placeholder');
    if (dict[key] !== undefined) {
      el.placeholder = dict[key];
    }
  });

  // Announce language change to screen readers
  const liveRegion = document.getElementById('ariaLive');
  if (liveRegion) {
    liveRegion.textContent = lang === 'de'
      ? 'Sprache auf Deutsch geändert'
      : 'Language changed to English';
  }

  // Update all [data-i18n-aria] elements
  document.querySelectorAll('[data-i18n-aria]').forEach(el => {
    const key = el.getAttribute('data-i18n-aria');
    if (dict[key] !== undefined) {
      el.setAttribute('aria-label', dict[key]);
    }
  });
}
